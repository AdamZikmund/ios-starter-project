import RealmSwift

// MARK: - Results
extension Results {

    /// Transforms results to array
    ///
    /// - Returns: Array of results elements
    func asArray() -> [Element] {
        return Array(self)
    }

}

// MARK: - List
extension List {

    /// Transforms list to array
    ///
    /// - Returns: Array of results elements
    func asArray() -> [Element] {
        return Array(self)
    }

}
